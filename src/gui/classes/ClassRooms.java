    @SuppressWarnings("unchecked")
    private void setTableData() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet rs = MySql.select("SELECT * FROM `classRooms`;");

            while (rs.next()) {
                Vector v = new Vector();

                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("location"));
                v.add(getClassCount(rs.getString("location")));

                dtm.addRow(v);
            }

        } catch (Exception ex) {
            l.log(Level.WARNING, "While inserting class room data.", ex);
        }
    }

    private String getClassCount(String id) throws Exception {
        ResultSet rs = MySql.select("SELECT * FROM `classes` WHERE `id` = '" + id + "' ; ");
        int count = 0;

        while (rs.next()) {
            count++;
        }

        return String.valueOf(count);
    }


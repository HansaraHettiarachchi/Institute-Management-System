    public Boolean validateFields() {

        name = jTextField1.getText();
        location = jTextField2.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter name for class ", emptyError(), JOptionPane.ERROR_MESSAGE);
        } else if (name.matches("^[a-zA-Z\\s\\-\\(\\)\"']+$")) {
            JOptionPane.showMessageDialog(this, "Please enter Name for class ", emptyError(), JOptionPane.ERROR_MESSAGE);
        } else if (location.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Location for class ", emptyError(), JOptionPane.ERROR_MESSAGE);
        } else if (location.matches("^[a-zA-Z\\s\\-\\(\\)\"']+$")) {
            JOptionPane.showMessageDialog(this, "Please enter Location for class ", emptyError(), JOptionPane.ERROR_MESSAGE);
        } else {
            return true;
        }
        return false;
    }

    private String emptyError() {
        return "Some fields are empty.";
    }


    private boolean checkAlreadyInserted(String text) {

        int rowCount = dtm.getRowCount();

        for (int i = 0; i < rowCount; i++) {
            if (dtm.getValueAt(i, 0).equals(text)) {
                return true;
            }
        }

        return false;
    }


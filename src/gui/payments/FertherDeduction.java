    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        JTextField textField = (JTextField) evt.getSource();
        char c = evt.getKeyChar();
        String text = textField.getText() + c;
        String regex = "^[a-zA-Z0-9()/\\s]{0,50}$";

        if (!text.matches(regex)) {
            evt.consume();
        }

    }//GEN-LAST:event_jTextField1KeyTyped

    @SuppressWarnings("unchecked")
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String desc = jTextField1.getText();
        String price = jTextField2.getText();

        if (desc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Discription.", "Empty Descrition", JOptionPane.WARNING_MESSAGE);
        } else if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Price.", "Empty Descrition", JOptionPane.WARNING_MESSAGE);
        } else if (checkAlreadyInserted(desc)) {
            JOptionPane.showMessageDialog(this, "Already Inserted.", "Repeated Value", JOptionPane.WARNING_MESSAGE);

        } else {
            double p = Double.valueOf(price);

            Vector v = new Vector();

            v.add(desc);
            v.add(p);

            dtm.addRow(v);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField1.grabFocus();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int rowCount = dtm.getRowCount();

        try {
            for (int i = 0; i < rowCount; i++) {
                MySql.iud("INSERT INTO `user_payments` (`desc`, `price`, `DAT`, `user_id`) VALUES (?,?,?,?);",
                        new Object[]{jTable1.getValueAt(i, 0), jTable1.getValueAt(i, 1),
                            new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date()), wId});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error conducted while intserting data.", "Try Again", JOptionPane.ERROR_MESSAGE);
            l.log(Level.WARNING, "While inserting data", e);
        } finally {
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


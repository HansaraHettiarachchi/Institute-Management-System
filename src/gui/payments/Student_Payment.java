    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String text = jTextField1.getText();
        sid = text;

        if (text.isEmpty() || !text.matches("^(?:\\d{8}|[vV]\\d{9}|\\d{12})$")) {
            JOptionPane.showMessageDialog(this, "PLease enter valide details to find.", " Invalied details.", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                ResultSet rs = MySql.select("SELECT * FROM `student` INNER JOIN `mobiles` ON `mobiles`.`student_id` = `student`.`id` WHERE `student`.`id` = '" + text + "' OR `nic` = '" + text + "' ; ");

                if (rs.next()) {
                    jLabel10.setText(rs.getString("fname") + " " + rs.getString("lname"));
                    jLabel11.setText(rs.getString("student.id"));
                    jLabel12.setText(rs.getString("email"));
                    jLabel13.setText(rs.getString("mobiles.sMob"));

                    String pMob = rs.getString("mobiles.pMobM");

                    if (pMob.isEmpty()) {
                        pMob = rs.getString("mobiles.pMobF");
                    }

                    jLabel14.setText(pMob);
                    setClassData(rs.getString("student.id"));
                }

            } catch (Exception ex) {
                l.log(Level.WARNING, "While setting student payment details.", ex);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String selectedText = String.valueOf(jComboBox1.getSelectedItem());

        if (!selectedText.equals("SELECT")) {
            try {
                ResultSet rs = MySql.select("SELECT * FROM `classes` WHERE `id` = '" + classData.get(selectedText) + "';");
                if (rs.next()) {
                    jLabel18.setText(Qube.df.format(rs.getDouble("fee")) + " LKR");
                    if (paidOrNot(selectedText)) {
                        jLabel16.setText("PAID");
                        jButton4.setEnabled(false);
                    } else {
                        jLabel16.setText("NOT PAID");
                        jButton4.setEnabled(true);
                    }
                }

            } catch (Exception ex) {
                l.log(Level.WARNING, "While setting class details.", ex);
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed


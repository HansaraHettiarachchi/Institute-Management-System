    private void printReport() {
        try {
//            String StudentName = jLabel10.getText();
            String selectedClass = jComboBox1.getSelectedItem().toString();
            String formattedDate = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date());

//            InputStream reportStream = this.getClass().getResourceAsStream("/Reports/studentReport.jasper");
            InputStream reportStream = getClass().getClassLoader().getResourceAsStream("Reports/feesInvoice.jasper");

            HashMap<String, Object> params = new HashMap<>();
            params.put("Parameter1", jLabel11.getText());
            params.put("Parameter2", jLabel10.getText());
            params.put("Parameter3", selectedClass);
            params.put("Parameter4", jLabel18.getText());
            params.put("Parameter5", formattedDate);

            JasperPrint jasperPrint = JasperFillManager.fillReport(reportStream, params, new JREmptyDataSource());

            JasperViewer.viewReport(jasperPrint, false);

            jButton4.setEnabled(false);
            jTextField1.setText("");
            jTextField1.grabFocus();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

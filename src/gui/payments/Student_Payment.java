    private Boolean paidOrNot(String class_id) throws Exception {

        ResultSet rs = MySql.select("SELECT * FROM `stupayments` WHERE `student_id` = '" + jLabel11.getText() + "' AND "
                + "`classes_id` = '" + classData.get(class_id) + "' AND `DAT` LIKE '%" + new SimpleDateFormat("yyyy-MM").format(new Date()) + "%';");

        if (rs.next()) {
            return true;
        }
        return false;
    }


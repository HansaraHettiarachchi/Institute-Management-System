    private void setClassData(String student_id) throws Exception {

        HashMap m1 = new Qube().getComboData("student_has_classes", "INNER JOIN `classes` ON `classes`.`id` = `student_has_classes`.`classes_id` WHERE `student_id` = '" + student_id + "'", "name");
        jComboBox1.setModel((ComboBoxModel<String>) m1.get(1));
        classData = (HashMap<String, Integer>) m1.get(2);

        HashMap m2 = new Qube().getComboData("paymentType", "", "name", false);
        jComboBox2.setModel((ComboBoxModel<String>) m2.get(1));
        paymentData = (HashMap<String, Integer>) m2.get(2);

        jComboBox1.setEnabled(true);
        jComboBox2.setEnabled(true);
    }


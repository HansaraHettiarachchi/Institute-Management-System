    private void loadPaymentHistory(String studentId) {
        try {
            String yearFilter = (String) jComboBox3.getSelectedItem();
            String monthFilter = (String) jComboBox4.getSelectedItem();

            String query = "SELECT sp.id, c.name AS class_name, sp.DAT, sp.price, pt.name AS payment_method "
                    + "FROM stupayments sp "
                    + "JOIN classes c ON sp.classes_id = c.id "
                    + "JOIN paymenttype pt ON sp.paymentType_id = pt.id "
                    + "WHERE sp.student_id = '" + studentId + "' ";

            if (yearFilter != null && !yearFilter.equals("All Years")) {
                query += "AND YEAR(sp.DAT) = '" + yearFilter + "' ";
            }

            if (monthFilter != null && !monthFilter.equals("All Months")) {
                int monthNumber = getMonthNumber(monthFilter);
                if (monthNumber > 0) {
                    query += "AND MONTH(sp.DAT) = " + monthNumber + " ";
                }
            }

            query += "ORDER BY sp.DAT DESC";

            ResultSet rs = MySql.select(query);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("class_name"),
                    rs.getString("DAT"),
                    rs.getDouble("price"),
                    rs.getString("payment_method")
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private int getMonthNumber(String monthName) {
        String[] monthNames = {"January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"};
        for (int i = 0; i < monthNames.length; i++) {
            if (monthNames[i].equals(monthName)) {
                return i + 1;
            }
        }
        return -1;
    }


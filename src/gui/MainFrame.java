    private void setLiveDate() {
        Thread t = new Thread(() -> {
            while (true) {
                try {
                    String dateTime = new SimpleDateFormat("EEEE, MMMM dd, yyyy hh:mm:ss a").format(new Date());
                    SwingUtilities.invokeLater(() -> jLabel10.setText(dateTime));
                    Thread.sleep(1000);
                } catch (InterruptedException ex) {
                    l.log(Level.WARNING, "While Setting live Date and time.", ex);
                }
            }
        });
        t.setDaemon(true);
        t.start();
    }


    private void sideBarTog() {
        int targetWidth = isSidebarExpanded ? COLLAPSED_WIDTH : EXPANDED_WIDTH;

        new Thread(() -> {
            try {
                int currentWidth = jPanel4.getWidth();
                int step = targetWidth > currentWidth ? 5 : -5;

                while ((step > 0 && currentWidth < targetWidth)
                        || (step < 0 && currentWidth > targetWidth)) {
                    currentWidth += step;

                    if ((step > 0 && currentWidth > targetWidth)
                            || (step < 0 && currentWidth < targetWidth)) {
                        currentWidth = targetWidth;
                    }

                    final int width = currentWidth;
                    SwingUtilities.invokeLater(() -> {
                        jPanel4.setSize(width, jPanel4.getHeight());
                        jLayeredPane1.setBounds(width, 0,
                                getContentPane().getWidth() - width,
                                jLayeredPane1.getHeight());
                        jPanel4.revalidate();
                        jLayeredPane1.revalidate();
                    });

                    Thread.sleep(10);
                }

                isSidebarExpanded = !isSidebarExpanded;
            } catch (InterruptedException e) {
                l.log(Level.WARNING, "Sidebar animation interrupted", e);
            }
        }).start();
    }

